#include "ft_num.h"

int	ft_numlen(ssize_t num)
{
	int	lng;

	if (num < 0)
		lng = 2;
	else
		lng = 1;
	while (num >= 10 || num <= -10)
	{
		num /= 10;
		lng++;
	}
	return (lng);
}
